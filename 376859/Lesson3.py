# –ó–∞–¥–∞—á–∞ 1. –î–∞–Ω—ã –∑–Ω–∞—á–µ–Ω–∏—è –∑–∞—Ä–ø–ª–∞—Ç –∏–∑ –≤—ã–±–æ—Ä–∫–∏ –≤—ã–ø—É—Å–∫–Ω–∏–∫–æ–≤: 100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150. –ü–æ—Å—á–∏—Ç–∞—Ç—å (–∂–µ–ª–∞—Ç–µ–ª—å–Ω–æ –±–µ–∑ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏—Ö –º–µ—Ç–æ–¥–æ–≤ –Ω–∞–ø–æ–¥–æ–±–∏–µ std, var, mean) —Å—Ä–µ–¥–Ω–µ–µ –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–µ, —Å—Ä–µ–¥–Ω–µ–µ –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ, —Å–º–µ—â–µ–Ω–Ω—É—é –∏ –Ω–µ—Å–º–µ—â–µ–Ω–Ω—É—é –æ—Ü–µ–Ω–∫–∏ –¥–∏—Å–ø–µ—Ä—Å–∏–π –¥–ª—è –¥–∞–Ω–Ω–æ–π –≤—ã–±–æ—Ä–∫–∏.
income = [100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150]
n = len(income)
Mx = sum(income)/n
print('Mx', Mx, mean(income)) # Mx 65.3 65.3
Dx0 = sum([pow(x-Mx, 2) for x in income])/n
Dx1 = sum([pow(x-Mx, 2) for x in income])/(n-1)
print('Dx0', Dx0, var(income)) # Dx0 950.11 950.11
print('Dx1', Dx1, var(income, ddof=1)) # Dx1 1000.1157894736842 1000.1157894736842
print('ùûº0', sqrt(Dx0), std(income)) # ùûº0 30.823854398825596 30.823854398825596
print('ùûº1', sqrt(Dx1), std(income, ddof=1)) # ùûº1 31.624607341019814 31.624607341019814



# –ó–∞–¥–∞—á–∞ 2. –í –ø–µ—Ä–≤–æ–º —è—â–∏–∫–µ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è 8 –º—è—á–µ–π, –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö 5 - –±–µ–ª—ã–µ. –í–æ –≤—Ç–æ—Ä–æ–º —è—â–∏–∫–µ - 12 –º—è—á–µ–π, –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö 5 –±–µ–ª—ã—Ö. –ò–∑ –ø–µ—Ä–≤–æ–≥–æ —è—â–∏–∫–∞ –≤—ã—Ç–∞—Å–∫–∏–≤–∞—é—Ç —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º –¥–≤–∞ –º—è—á–∞, –∏–∑ –≤—Ç–æ—Ä–æ–≥–æ - 4 –º—è—á–∞. –ö–∞–∫–æ–≤–∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ 3 –º—è—á–∞ –±–µ–ª—ã–µ?
def combinations(n, k):
    return factorial(n) / (factorial(k) * factorial(n-k))

n1 = combinations(8, 2); n2 = combinations(12, 4);
def PA1xPA2(box1white, box2white): 
    m1 = combinations(5, box1white) * combinations(3, 2-box1white)
    PA1 = m1/n1
    m2 = combinations(5, box2white) * combinations(7, 4-box2white)
    PA2 = m2/n2
    return PA1*PA2

PA = 0
for i in [[0,3],[1,2],[2,1]]:
    PA += PA1xPA2(i[0], i[1])
print('PA', PA) # PA 0.3686868686868687



